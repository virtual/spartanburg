              <h1 class="acalog-program-name">Administrative Accounting Specialist Certificate</h1>
              <div class="acalog-program-description">
                <p><strong>Program Start Date:</strong> Fall or spring terms<br>
                  <strong>Minimum Program Length:</strong> 32 academic weeks; 2 terms day, 3 terms evening; 28
                  credits<br>
                  <strong>Curriculum Code: 70922</strong></p>

                <h2>Program Description</h2>

                <p>Accounting specialist students develop basic accounting skills to analyze, record, summarize
                  and report accounting information. A comprehensive study of payroll accounting procedures,
                  individual income tax procedures, Excel spreadsheet applications, and computerized accounting
                  software applications are included. Students focus on communication, general office procedures
                  and professional development.</p>

                <h2>Practical Experience</h2>

                <p>Students complete accounting simulations using microcomputers, develop accounting models
                  using Excel spreadsheets, and perform accounting applications using integrated accounting
                  software. Projects are assigned that simulate actual applications in today's offices, allowing
                  students to develop individual software skills. Effective communication, team building and
                  problem-solving skills will be stressed.</p>

                <h2>Professional Opportunities</h2>

                <p>Accounting clerk, payroll clerk, bookkeeper, billing clerk, accounts receivable clerk,
                  accounts payable clerk, office assistant, inventory control clerk, administrative specialist
                  and tax preparer.</p>

                <h2>Unique Aspects</h2>

                <p>Graduates of this program may transfer credits into the Accounting associate degree program.
                </p>

                <h2>EEDA Career Cluster:</h2>

                <p>Government &amp; Public Administration; Business, Management &amp; Administration; Finance
                </p>
